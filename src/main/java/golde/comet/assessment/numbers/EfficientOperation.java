package golde.comet.assessment.numbers;

public class EfficientOperation {

    public static void main(String[] args) {
        float a = 1.0f;
        float b = 0.9f;
        float c = 0.8f;
        operation(a, b, c);
    }

    public static void operation(float a, float b, float c) {
        System.out.println(a * b + c);
    }
}
